// US jurisdictions. Jurisdiction is a first-class intake field because the
// applicable right-of-publicity statute depends on it (Phase 2).
//
// `COVERED_STATES` are the v1 set with tailored statute coverage. Every other
// state is selectable but labeled "general — verify locally". The instrument
// library that uses this lands in Phase 2; this file only marks the boundary.

export const COVERED_STATES = ["TN", "WA", "CA", "NY"] as const;

export type CoveredState = (typeof COVERED_STATES)[number];

export function isCoveredState(code: string): boolean {
  return (COVERED_STATES as readonly string[]).includes(code);
}

